<?php
namespace PromisePay\DataObjects;
/**
 * Class ItemStatus
 * @package PromisePay\DataObjects
 */
class ItemStatus
{
    /**
     * @var
     */
    public $_id;
    /**
     * @var
     */
    public $_status;
    /**
     * @var
     */
    public $_state;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->_id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->_id = $id;
    }

    /**
     * @return mixed
     */
    public function getState()
    {
        return $this->_state;
    }

    /**
     * @param mixed $state
     */
    public function setState($state)
    {
        $this->_state = $state;
    }

    /**
     * @return mixed
     */
    public function getStatus()
    {
        return $this->_status;
    }

    /**
     * @param mixed $status
     */
    public function setStatus($status)
    {
        $this->_status = $status;
    }

}